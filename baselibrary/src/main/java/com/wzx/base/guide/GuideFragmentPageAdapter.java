package com.wzx.base.guide;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

public class GuideFragmentPageAdapter extends FragmentPagerAdapter {
    private List<GuideFragment> mFragments;
    public GuideFragmentPageAdapter(FragmentManager fm, List<GuideFragment> fragments) {
        super(fm);
        this.mFragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return mFragments.get(position);
    }

    @Override
    public int getCount() {
        return mFragments.size();
    }
}
